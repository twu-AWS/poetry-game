def create_database():
    """创建DuckDB数据库并插入数据"""
    
    # 连接数据库（如果不存在会自动创建）
    conn = duckdb.connect('poetry_game.db')
    
    # 创建表
    conn.execute("""
        CREATE TABLE IF NOT EXISTS poems (
            id INTEGER PRIMARY KEY,
            full_text VARCHAR,
            question VARCHAR,
            answer VARCHAR,
            options VARCHAR,
            title VARCHAR,
            type VARCHAR
        )
    """)
    
    # 清空表（如果重新初始化）
    conn.execute("DELETE FROM poems")
    
    # 插入数据
    for i, (full, question, answer, options, title, ptype) in enumerate(POETRY_DATA, 1):
        conn.execute("""
            INSERT INTO poems (id, full_text, question, answer, options, title, type)
            VALUES (?, ?, ?, ?, ?, ?, ?)
        """, (i, full, question, answer, options, title, ptype))
    
    # 提交并关闭
    conn.commit()
    
    # 验证数据
    result = conn.execute("SELECT COUNT(*) as count, type FROM poems GROUP BY type").fetchall()
    print("✅ 数据库创建成功!")
    print(f"📚 题库统计:")
    for count, ptype in result:
        print(f"   {ptype}: {count}道题")
    
    total = conn.execute("SELECT COUNT(*) FROM poems").fetchone()[0]
    print(f"   总计: {total}道题")
    
    conn.close()


if __name__ == "__main__":
    create_database()
